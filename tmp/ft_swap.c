# include <stdio.h>

void    ft_swap(int *a, int *b)
{
    int tmp = *a;
    *a = *b;
    *b = tmp;
}

int main(void)
{
    int a = 4755;
    int b = 884;

    ft_swap(&a, &b);
    printf("%d\n", a);
    printf("%d\n", b);
    return (0);
}